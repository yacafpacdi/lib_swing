    private void txtRechercheKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtRechercheKeyReleased
        // TODO add your handling code here:

        try{
            String req = "SELECT idTheme AS ID, nomTheme AS THEME,actifTheme AS ACTIF"
            + " from Theme"
            + " where nomTheme like ?";
            ResultSet rs = theme.rechercher(req,"%"+txtRecherche.getText()+"%");
            jTableTheme.setModel(DbUtils.resultSetToTableModel(rs));
        }catch(Exception e){

            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_txtRechercheKeyReleased

    private void jTableThemeMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableThemeMouseClicked
        try {
            int row = jTableTheme.getSelectedRow();
            String Table_click = (jTableTheme.getModel().getValueAt(row, 0).toString());
            String req = "SELECT idTheme, nomTheme,actifTheme"
            + " from Theme WHERE IdTheme='" + Table_click + "' ";
            ResultSet rs = theme.selectionner(req);
            if (rs.next()) {
                String add1 = rs.getString("idTheme");
                fen.getIfTheme().getTxtId().setText(add1);
                String add2 = rs.getString("nomTheme");
                fen.getIfTheme().getTxtTheme().setText(add2);
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);

        }
    }//GEN-LAST:event_jTableThemeMouseClicked


