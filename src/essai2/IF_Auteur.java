        public void afficherTable() throws SQLException, Exception {

        try {
            String req = "SELECT idAuteur AS ID, nomAuteur AS NOM,"
                    + " prenomAuteur AS PRENOM, dateNaissanceAuteur AS NAISS,"
                    + " lieuNaissanceAuteur AS LIEU, actifAuteur AS ACTIF"
                    + " from Auteur";
            ResultSet rs = auteur.selectionner(req);
            fen.getIfAAuteur().getjTableAuteur().setModel(DbUtils.resultSetToTableModel(rs));
        } catch (Exception e) {

            JOptionPane.showMessageDialog(null, e);
        }
    }

